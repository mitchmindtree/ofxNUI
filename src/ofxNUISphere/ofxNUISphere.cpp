#include "ofxNUISphere.h"

//--------------------------------------------------------------------------------//
// SETUP SPHERE
//--------------------------------------------------------------------------------//

void ofxNUISphere::setup(float radius)
{
    spherePrimitive.set(radius, 5);
    spherePrimitive.setOrientation(position);
    mesh = spherePrimitive.getMesh();
}

//--------------------------------------------------------------------------------//
// SET COLOUR
//--------------------------------------------------------------------------------//

void ofxNUISphere::setColour(ofColor color)
{
    for (int i=0; i < mesh.getVertices().size(); i++) {
        mesh.addColor(color+i);
    }
}

//--------------------------------------------------------------------------------//
// SET POSITION
//--------------------------------------------------------------------------------//

void ofxNUISphere::setPosition(ofVec3f _position)
{
    position = _position;
}

//--------------------------------------------------------------------------------//
// DRAW SPHERE
//--------------------------------------------------------------------------------//

void ofxNUISphere::draw()
{
    ofPushMatrix();
    ofTranslate(position);
    ofRotate(wave1, 0.0f, ang, 1.0f);
    //mesh.drawFaces();
    mesh.drawWireframe();
    ofPopMatrix();
}

//--------------------------------------------------------------------------------//
// UPDATE ANGLE
//--------------------------------------------------------------------------------//

void ofxNUISphere::updateAngle()
{
    ang+=0.1f;
    wave1 = 100.0f * sin(ang*0.1f) + ang;
}

